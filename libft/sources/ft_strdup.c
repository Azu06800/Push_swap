#include "../includes/libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		dest_len;

	dest_len = ft_strlen(s) + 1;
	dest = (char *) ft_calloc(dest_len, 1);
	if (!dest)
		return (NULL);
	return (ft_strcpy(dest, s));
}
